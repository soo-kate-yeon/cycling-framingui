import { mkdirSync, readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';
import prettier from 'prettier';

const scriptDir = dirname(fileURLToPath(import.meta.url));
const packageDir = join(scriptDir, '..');
const repoDir = join(packageDir, '..', '..');
const themesDir = join(repoDir, '.moai', 'themes', 'generated');
const outputFile = join(packageDir, 'src', 'generated', 'bundled-themes.ts');

const files = readdirSync(themesDir)
  .filter((file) => file.endsWith('.json'))
  .sort((a, b) => a.localeCompare(b));

const themes = files.map((file) => {
  const theme = JSON.parse(readFileSync(join(themesDir, file), 'utf8'));
  return theme;
});

const metadata = themes.map(({ id, name, description, brandTone, schemaVersion }) => ({
  id,
  name,
  description,
  brandTone,
  schemaVersion,
}));

const source = `// Auto-generated by scripts/generate-bundled-theme-module.mjs
// Do not edit by hand.

import type { ThemeMetaV2, ThemeV2 } from '../theme-v2.js';

export const BUNDLED_THEMES = ${JSON.stringify(
  Object.fromEntries(themes.map((theme) => [theme.id, theme])),
  null,
  2
)} as unknown as Record<string, ThemeV2>;

export const BUNDLED_THEME_LIST = ${JSON.stringify(metadata, null, 2)} as unknown as ThemeMetaV2[];
`;

const formattedSource = await prettier.format(source, {
  ...(await prettier.resolveConfig(outputFile)),
  filepath: outputFile,
});

mkdirSync(dirname(outputFile), { recursive: true });
writeFileSync(outputFile, formattedSource);
